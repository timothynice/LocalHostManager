#!/bin/zsh

: "${APP_BINARY_NAME:=Studi0Ports}"
: "${APP_BUNDLE_NAME:=Studi0 Ports}"
: "${APP_DISPLAY_NAME:=$APP_BUNDLE_NAME}"
: "${BUNDLE_ID:=com.studi0ports.app}"
: "${MIN_MACOS_VERSION:=14.0}"
: "${BUILD_CONFIGURATION:=release}"

if [[ -z "${APP_VERSION:-}" ]]; then
  if [[ -f "$ROOT_DIR/VERSION" ]]; then
    APP_VERSION="$(tr -d '[:space:]' < "$ROOT_DIR/VERSION")"
  else
    APP_VERSION="0.1.0"
  fi
fi

: "${BUILD_NUMBER:=1}"
: "${RELEASE_TAG:=v$APP_VERSION}"

DIST_DIR="$ROOT_DIR/dist"
APP_DIR="$DIST_DIR/$APP_BUNDLE_NAME.app"
APP_CONTENTS_DIR="$APP_DIR/Contents"
MACOS_DIR="$APP_CONTENTS_DIR/MacOS"
RESOURCES_DIR="$APP_CONTENTS_DIR/Resources"
INFO_PLIST_PATH="$APP_CONTENTS_DIR/Info.plist"

ICON_SOURCE_PATH="$ROOT_DIR/Branding/studi0-ports-icon-512.png"
ICONSET_DIR="$ROOT_DIR/.build/AppIcon.iconset"
ICON_ICNS_PATH="$ROOT_DIR/.build/AppIcon.icns"

BINARY_PATH="$ROOT_DIR/.build/$BUILD_CONFIGURATION/$APP_BINARY_NAME"
ZIP_PATH="$DIST_DIR/$APP_BINARY_NAME-$APP_VERSION.zip"
DMG_PATH="$DIST_DIR/$APP_BINARY_NAME-$APP_VERSION.dmg"
CHECKSUM_PATH="$DIST_DIR/$APP_BINARY_NAME-$APP_VERSION-sha256.txt"
